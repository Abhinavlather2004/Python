# For loop with Lists
l = [1, 2, 3, 4, 5, 9]
for i in l:
    print(i)

# For loop with Tuples
t = (1, 2, 3, 4, 5, 9)
for i in t:
    print(i)

# For loop with Strings
s = "hello world"
for i in s:
    print(i)

for i in range(len(s)):
    print(s[i])

# For loop with Dictionaries
d = {"name": "John", "age": 30, "city": "New York"}
for key in d:
    print(key, ":", d[key])

# For loop with Sets
s = {1, 2, 3, 4, 5, 9}
for i in s:
    print(i)